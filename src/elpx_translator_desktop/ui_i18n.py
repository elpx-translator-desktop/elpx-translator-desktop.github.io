def detect_ui_language() -> str:
    candidates = [
        locale.getlocale()[0],
        locale.getdefaultlocale()[0] if hasattr(locale, 'getdefaultlocale') else None,
    ]
    for candidate in candidates:
        if not candidate:
            continue
        code = candidate.split('_', 1)[0].lower()
        if code in SUPPORTED_UI_LANGUAGES:
            return code
    return 'es'


def tr(language: str, key: str, **kwargs) -> str:
    language = language if language in TRANSLATIONS else 'es'
    value = TRANSLATIONS[language].get(key) or TRANSLATIONS['es'].get(key) or key
    return value.format(**kwargs)


def performance_label(language: str, mode: str) -> str:
    language = language if language in PERFORMANCE_MODE_I18N else 'es'
    return PERFORMANCE_MODE_I18N[language].get(mode, mode)
